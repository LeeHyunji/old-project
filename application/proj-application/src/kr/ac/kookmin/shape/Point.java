package kr.ac.kookmin.shape;

public class Point {
	private int x;
	private int y;
	
	public Point(int x, int y){
		this.x=x;
		this.y=y;
	}
	public int getX(){
		return this.x;
	}
	public void setX(){
		this.x=x;
	}
	public int getY(){
		return this.y=y;
	}
	public void setY(){
		this.y=y;
	}
}